#include <stdio.h>
#include <unistd.h>
/**
 * main - Entry point
 * Description: prints lowercase alphabets
 * Return: Always 0 (success)
 **/
int main(void)
{
	int bc = 'a';

	for (bc = 'a'; bc <= 'z'; bc++)
	{
	if (bc != 'e'&& bc = 'q')
	{
	putchar(bc);
	}
	else
	{
	putchar(bc);
	}
	}
	putchar('\n');
	return (0);
}
